class main {
    public static int SUM(int a, int b) {
        return a + b;
    }
    public static double sum(double a, double b) {
        return a + b;
    }
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        double c = 5.5;
        double d = 10.5;
        System.out.println(SUM(a, b));
        System.out.println(sum(c, d));
    }
}
